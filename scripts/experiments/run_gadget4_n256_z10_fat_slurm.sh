from pathlib import Path
import json

import h5py

run_dir = Path("outputs/gadget4_dmo_n256_z10_fat")
expected = 256 ** 3
snapshots = sorted((run_dir / "output").glob("snapshot_*.hdf5"))
if len(snapshots) != 5:
    raise RuntimeError(f"expected 5 snapshots, found {len(snapshots)}")

checks = []
for snapshot in snapshots:
    with h5py.File(snapshot, "r") as handle:
        count = handle["PartType1/ParticleIDs"].shape[0]
        if count != expected:
            raise RuntimeError(f"{snapshot} has {count} particles, expected {expected}")
        checks.append(
            {
                "snapshot": str(snapshot),
                "time": float(handle["Header"].attrs["Time"]),
                "redshift": float(handle["Header"].attrs["Redshift"]),
                "dm_particles": count,
            }
        )

print(json.dumps({"validated_snapshots": checks}, indent=2, sort_keys=True))
PY
